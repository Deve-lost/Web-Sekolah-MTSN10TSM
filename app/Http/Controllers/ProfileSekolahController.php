    {
        // Validasi
        $this->validate($request, [
            'judul' => 'required',
            'deskripsi' => 'required',
            'image' => 'required',
        ]);
        
        $post = Sekolah::create([
            'user_id' => auth()->user()->id,
            'judul' => $request->judul,
            'deskripsi' => $request->deskripsi,
            'image' => $request->image
        ]);

        return redirect()->route('profile.sekolah')->with('sukses', 'Data Berhasil Ditambahkan');
    }

